def run_dea_ccr(X: np.ndarray, Y: np.ndarray) -> np.ndarray:
    """
    CCR Input-Oriented DEA menggunakan scipy.optimize.linprog.

    Parameters
    ----------
    X : ndarray (n, m)  — matrix input  (n DMU, m variabel input)
    Y : ndarray (n, s)  — matrix output (n DMU, s variabel output)

    Returns
    -------
    scores : ndarray (n,)  — skor efisiensi [0, 1]
    """
    n, m = X.shape
    s    = Y.shape[1]
    scores = []

    for k in range(n):
        # Variabel optimasi: [θ, λ_0, λ_1, …, λ_{n-1}]
        c = np.zeros(n + 1)
        c[0] = 1.0   # minimize θ

        # Constraint input: -X[k,i]·θ + Σ X[j,i]·λ_j ≤ 0
        A_in = np.zeros((m, n + 1))
        b_in = np.zeros(m)
        for i in range(m):
            A_in[i, 0]  = -X[k, i]     # koefisien θ
            A_in[i, 1:] =  X[:, i]     # koefisien λ

        # Constraint output: -Σ Y[j,r]·λ_j ≤ -Y[k,r]
        A_out = np.zeros((s, n + 1))
        b_out = -Y[k, :]
        for r in range(s):
            A_out[r, 1:] = -Y[:, r]    # koefisien λ

        A_ub = np.vstack([A_in,  A_out])
        b_ub = np.hstack([b_in,  b_out])
        bounds = [(0, None)] * (n + 1)

        res = linprog(c, A_ub=A_ub, b_ub=b_ub, bounds=bounds, method="highs")
        scores.append(float(np.clip(res.fun, 0, 1)) if res.success else np.nan)

    return np.array(scores)

