def batch_predict(df_new: pd.DataFrame) -> pd.DataFrame:
    """
    Prediksi nilai produksi untuk banyak petani sekaligus.

    Parameters
    ----------
    df_new : DataFrame dengan kolom sesuai RF_FEATURE_COLS
             (tidak perlu sudah di-scale; fungsi ini menangani scaling)

    Returns
    -------
    DataFrame dengan kolom tambahan:
      predicted_production, estimated_profit, profit_margin_pct, efficiency_cat
    """
    df_new = df_new.copy()

    # Feature engineering jika belum ada
    if "total_input_cost" not in df_new.columns:
        farmer_inp_avail = [c for c in FARMER_INPUTS if c in df_new.columns]
        df_new["total_input_cost"] = df_new[farmer_inp_avail].sum(axis=1)
    if "cost_per_m2" not in df_new.columns:
        df_new["cost_per_m2"] = (df_new["total_input_cost"]
                                 / df_new["Land area (m2)"].replace(0, np.nan))
    if "fertilizer_ratio" not in df_new.columns:
        df_new["fertilizer_ratio"] = (df_new["Fertilizer purchase value (IDR)"]
                                      / df_new["total_input_cost"].replace(0, np.nan))
    if "labor_ratio" not in df_new.columns:
        df_new["labor_ratio"] = (df_new["Labor cost (IDR)"]
                                 / df_new["total_input_cost"].replace(0, np.nan))
    if "region_encoded" not in df_new.columns:
        df_new["region_encoded"] = df_new["region"].map(region_map).fillna(-1)

    X_new    = df_new[RF_FEATURE_COLS].fillna(0).values
    X_new_sc = _scaler_rf.transform(X_new)
    preds    = _rf_model.predict(X_new_sc)

    df_new["predicted_production"] = preds
    df_new["estimated_profit"]     = preds - df_new["total_input_cost"]
    df_new["profit_margin_pct"]    = (df_new["estimated_profit"]
                                      / df_new["predicted_production"].replace(0, np.nan) * 100)
    df_new["efficiency_cat"] = pd.cut(
        df_new["profit_margin_pct"],
        bins=[-np.inf, 25, 50, np.inf],
        labels=["Rendah", "Sedang", "Tinggi"]
    )
    return df_new


