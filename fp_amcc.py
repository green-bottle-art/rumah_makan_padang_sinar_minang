def gross_margin_trader(df, purchase_col, output_col, actor_name):
    """
    Gross margin: hanya harga jual vs harga beli beras.
    Tidak memasukkan overhead karena berbeda periode pengukuran.
    """
    d = df.copy()
    d.columns = [c.strip() for c in d.columns]

    if purchase_col not in d.columns or output_col not in d.columns:
        print(f"⚠️  [{actor_name}] Kolom tidak ditemukan!")
        return pd.Series([np.nan]*len(REGIONS), index=REGIONS)

    d["_gross_margin"] = (
        (d[output_col] - d[purchase_col])
        / d[output_col].replace(0, np.nan) * 100
    )
    result = d.groupby("region")["_gross_margin"].mean().reindex(REGIONS)

    print(f"\n✅ [{actor_name}] Gross Margin per wilayah:")
    for region, val in result.items():
        if not np.isnan(val):
            status = "✅" if val > 0 else "❌"
            print(f"   {region:<12}: {val:6.1f}%  {status}")
    return result


