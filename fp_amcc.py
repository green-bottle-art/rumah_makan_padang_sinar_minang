# ── 5.2 Penanganan Outlier (IQR Capping) ──────────────────────────────────
def cap_outliers_iqr(df: pd.DataFrame, cols: list) -> pd.DataFrame:
    """Capping outlier pada batas IQR 1.5× (tidak dihapus)."""
    df = df.copy()
    report = {}
    for col in cols:
        Q1  = df[col].quantile(0.25)
        Q3  = df[col].quantile(0.75)
        IQR = Q3 - Q1
        lo  = Q1 - 1.5 * IQR
        hi  = Q3 + 1.5 * IQR
        n_before = ((df[col] < lo) | (df[col] > hi)).sum()
        df[col]  = df[col].clip(lower=lo, upper=hi)
        report[col] = n_before
    n_total = sum(report.values())
    print(f"  → {n_total} nilai di-cap pada kolom: "
          f"{[k for k, v in report.items() if v > 0]}")
    return df

