def parse_regional_sheet(file_path: str,
                         sheet_name: str,
                         actor_type: str) -> pd.DataFrame:
    """
    Mem-parse satu sheet Excel multi-wilayah menjadi satu DataFrame bersih.

    Setiap sheet mengandung sub-tabel per wilayah yang dipisahkan oleh
    baris nama wilayah. Fungsi ini mendeteksi dan menggabungkan semua
    sub-tabel tersebut.

    Parameters
    ----------
    file_path  : str  — path ke file .xlsx
    sheet_name : str  — nama sheet yang akan di-parse
    actor_type : str  — label aktor ('Farmer', 'Miller', dll.)

    Returns
    -------
    pd.DataFrame dengan kolom tambahan 'region' dan 'actor_type'
    """
    # Baca tanpa header supaya bisa mengontrol parsing sendiri
    df_raw = pd.read_excel(file_path, sheet_name=sheet_name, header=None)

    collected   = []      # list of DataFrame per wilayah
    cur_region  = None
    header_cols = None
    row_buffer  = []

    for _, row in df_raw.iterrows():
        first_cell = str(row.iloc[0]).strip() if pd.notna(row.iloc[0]) else ""

        # ── Deteksi baris nama wilayah ────────────────────────────────────
        if first_cell in REGIONS:
            # Simpan buffer wilayah sebelumnya
            if cur_region and header_cols and row_buffer:
                tmp = pd.DataFrame(row_buffer, columns=header_cols)
                tmp.insert(0, "region",     cur_region)
                tmp.insert(1, "actor_type", actor_type)
                collected.append(tmp)
            cur_region  = first_cell
            header_cols = None
            row_buffer  = []

        # ── Deteksi baris header (kolom pertama = 'dmu') ──────────────────
        elif first_cell.lower() == "dmu" and cur_region is not None:
            header_cols = [str(v).strip() for v in row if pd.notna(v)]

        # ── Deteksi baris data (kolom pertama berupa angka) ───────────────
        elif cur_region is not None and header_cols is not None:
            try:
                float(first_cell)
                vals = list(row)[: len(header_cols)]
                if len(vals) == len(header_cols):
                    row_buffer.append(vals)
            except (ValueError, TypeError):
                pass  # Abaikan baris kosong / komentar

    # Simpan wilayah terakhir di sheet
    if cur_region and header_cols and row_buffer:
        tmp = pd.DataFrame(row_buffer, columns=header_cols)
        tmp.insert(0, "region",     cur_region)
        tmp.insert(1, "actor_type", actor_type)
        collected.append(tmp)

    if not collected:
        print(f"  ⚠️  Sheet '{sheet_name}' tidak menghasilkan data.")
        return pd.DataFrame()

    result = pd.concat(collected, ignore_index=True)

    # Konversi semua kolom (kecuali region & actor_type) ke numerik
    skip = {"region", "actor_type"}
    for col in result.columns:
        if col not in skip:
            result[col] = pd.to_numeric(result[col], errors="coerce")

    return result



